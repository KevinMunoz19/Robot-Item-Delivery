function T = ur3FK(q)
    DH = [ q(1)-pi/2 , 0.1519   , 0       ,      pi/2; 
           q(2)-pi/2 , 0        , -0.24365,      0; 
           q(3)      ,      0   , -0.21325,      0; 
           q(4)-pi/2 , 0.11235  ,  0      ,      pi/2; 
           q(5)      ,   0.08535,  0      ,      -pi/2; 
           q(6)      ,    0.0819,   0     ,      0    ];
    Et_G = [-0.00042241812,-0.00010805060,0.15907252]';
    Erpy_G = [0.00010711576,1.5644112,-0.00010695976]';
    ER_G = rpy2r(Erpy_G(1),Erpy_G(2),Erpy_G(3),'xyz');
    ET_G = [ER_G, Et_G; 0,0,0,1];
    T = getFK(DH)*ET_G; 
end